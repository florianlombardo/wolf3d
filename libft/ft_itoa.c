#include "libft.h"

char	*ft_itoa(int n)
{
	char	*tab;
	size_t	i;

	if (!(tab = ft_strnew(ft_intnsize(n + 1))))
		return (0);
	i = ft_intnsize(n) - 1;
	if (n == -2147483648)
	{
		n = n / 10;
		tab[i--] = '8';
	}
	if (n < 0)
	{
		tab[0] = '-';
		n = -n;
	}
	n == 0 ? tab[0] = '0' : 0;
	while (n != 0)
	{
		tab[i--] = n % 10 + '0';
		n = n / 10;
	}
	return (tab);
}
