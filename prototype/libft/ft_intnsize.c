#include "libft.h"

size_t	ft_intnsize(int n)
{
	size_t i;

	i = 0;
	n < 0 ? i = 1 : i;
	n < 0 ? n = -n : n;
	n == 0 ? i = 2 : i;
	while (n != 0)
	{
		n = n / 10;
		i++;
	}
	return (i);
}
