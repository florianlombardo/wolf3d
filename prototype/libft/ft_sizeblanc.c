#include "libft.h"

size_t	ft_sizeblanc(char const *s)
{
	size_t i;
	size_t j;

	i = 0;
	while (s[i] && (s[i] == '\t' || s[i] == '\n' || s[i] == ' '))
		i++;
	j = ft_strlen(s) - 1;
	while (j > i && (s[j] == '\t' || s[j] == '\n' || s[j] == ' '))
		j--;
	return (j - i + 1);
}
